// Compile the paywall's access keys — plain F1-meme passphrases.
//
//   node scripts/gen-access-keys.mjs                         recompile hashes from the current list
//   node scripts/gen-access-keys.mjs "kimi leave me alone"   append one or more new phrases
//   node scripts/gen-access-keys.mjs --reset                 reset to the built-in defaults
//
// The editable master list is secrets/access-keys.txt (one phrase per line,
// gitignored). This script reads it (seeding from the defaults below on first
// run), appends any phrases passed as arguments, de-dupes, and writes:
//   secrets/access-keys.txt        — the phrases to hand out
//   src/data/accessKeyHashes.ts    — SHA-256 hashes the app checks against (committed)
//
// Keys are intentionally guessable memes (a friends-and-family project). To hand
// one out, just tell someone a phrase. Matching ignores case/spaces/punctuation.

import crypto from 'node:crypto'
import fs from 'node:fs'
import path from 'node:path'

const DEFAULT_MEMES = [
  'Bono my tyres are gone',
  'Must be the water',
  'Lights out and away we go',
  'Is that Glock',
  'Leave me alone I know what I am doing',
  'Get in there Lewis',
  'Smooth operator',
  'It is called a motor race',
  'GP2 engine GP2',
  'Multi 21',
  'Valtteri it is James',
  'Simply lovely',
  'Hammer time',
  'Box box box',
  'Purple sector',
  'We are checking',
  'No Mikey no Mikey',
  'Felipe baby stay cool',
  'I am stupid',
  'Mamma mia',
  'Porca miseria',
  'Oh no no no',
  'Tea no thanks',
  'Yuki get out of the way',
  'We go again',
  'Blue flags',
  'Push push push',
  'Stay out stay out',
  'The pace is good',
  'I was racing',
  'Deg is too high',
  'To be the man you beat the man',
  'Checo',
]

const normalize = (k) => k.toUpperCase().replace(/[^0-9A-Z]/g, '')
const sha256 = (s) => crypto.createHash('sha256').update(s).digest('hex')

const root = path.resolve(import.meta.dirname, '..')
const keysPath = path.join(root, 'secrets', 'access-keys.txt')
const hashesPath = path.join(root, 'src', 'data', 'accessKeyHashes.ts')

const reset = process.argv.includes('--reset')
const added = process.argv.slice(2).filter((a) => !a.startsWith('--'))

const readLines = (p) =>
  fs.existsSync(p)
    ? fs.readFileSync(p, 'utf8').split('\n').map((l) => l.trim()).filter((l) => l && !l.startsWith('#'))
    : null

const base = reset ? DEFAULT_MEMES : (readLines(keysPath) ?? DEFAULT_MEMES)
const phrases = [...base, ...added]

// De-dupe by normalised form, keeping the first (nicely-cased) spelling.
const seen = new Set()
const list = []
for (const p of phrases) {
  const n = normalize(p)
  if (n && !seen.has(n)) {
    seen.add(n)
    list.push(p)
  }
}

fs.mkdirSync(path.dirname(keysPath), { recursive: true })
fs.writeFileSync(
  keysPath,
  `# f1buddy access keys — ${list.length} phrases, updated ${new Date().toISOString()}\n` +
    `# Edit freely: add a line to issue a phrase, delete one to revoke, then re-run this script.\n` +
    `# A phrase works on all of a person's devices and never expires.\n\n` +
    list.join('\n') +
    '\n',
)

fs.mkdirSync(path.dirname(hashesPath), { recursive: true })
fs.writeFileSync(
  hashesPath,
  `// AUTO-GENERATED by scripts/gen-access-keys.mjs — do not edit by hand.\n` +
    `// SHA-256 hashes of the normalised access phrases. Revoke a phrase by removing\n` +
    `// it from secrets/access-keys.txt and re-running the script, then redeploy.\n` +
    `export const ACCESS_KEY_HASHES: string[] = [\n` +
    list.map((p) => `  '${sha256(normalize(p))}',`).join('\n') +
    `\n]\n`,
)

console.log(`Compiled ${list.length} phrases${added.length ? ` (+${added.length} new)` : ''}.`)
console.log(`  secrets/access-keys.txt · src/data/accessKeyHashes.ts\n`)
console.log(list.join('\n'))
